                          ),
                          child: Icon(
                            Icons.edit,
                            color: Colors.white,
                          ),
                        )),
                  ],
                ),
              ),
              SizedBox(
                height: 35,
              ),
              buildTextField("Nome completo", "Jorge Jesus", false),
              buildTextField("Email", "jj@gmail.com", false),
